/**
 * Generates lib/ui-copy-locales.ts from scripts/locale-partials.mjs
 * Run: node scripts/generate-ui-locales.mjs
 */
import { writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';
import { LOCALE_PARTIALS } from './locale-partials.mjs';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const outPath = join(root, 'lib', 'ui-copy-locales.ts');

const codes = Object.keys(LOCALE_PARTIALS).sort();

const header = `/** Auto-generated by scripts/generate-ui-locales.mjs — do not edit by hand. */
import type { LanguageCode } from './languages';
import { EN, patch, type UiCopy } from './ui-copy-base';

function L(partial: Parameters<typeof patch>[1]): UiCopy {
  return patch(EN, partial);
}

`;

const entries = codes
  .map((code) => {
    const constName = code.replace(/-/g, '_').toUpperCase();
    return `const ${constName} = L(${JSON.stringify(LOCALE_PARTIALS[code], null, 2)} as Parameters<typeof patch>[1]);`;
  })
  .join('\n\n');

const mapEntries = ['en: EN', ...codes.map((c) => {
  const constName = c.replace(/-/g, '_').toUpperCase();
  return `${JSON.stringify(c)}: ${constName}`;
})].join(',\n  ');

const footer = `
export const LOCALE_COPIES: Partial<Record<LanguageCode, UiCopy>> = {
  ${mapEntries},
};
`;

writeFileSync(outPath, header + entries + footer, 'utf8');
console.log(`Wrote ${outPath} (${codes.length + 1} locales)`);
